#include "rush.h"

int	get_color(int nb)
{
	int i;

	if (nb == 0)
		return (1);
	i = 0;
	while (nb != 1)
	{
		nb /= 2;
		i++;
	}
	i = (i % 5) + 2;
	return (i);
}
