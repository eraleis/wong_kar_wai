int		ft_atoi(const char *str)
{
	int		i;
	int		neg;
	int		n;

	i = 0;
	neg = 1;
	n = 0;
	while (((str[i] > 8) && (str[i] < 14)) || (str[i] == 32))
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	if ((str[i] == '+') && (neg != -1))
		i++;
	while (str[i] > 47 && str[i] < 58)
	{
		n = 10 * n;
		n = n + (str[i] - 48);
		i++;
	}
	return (neg * n);
}
